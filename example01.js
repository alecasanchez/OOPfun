// object literal
const circle = {
  radius: 1,
  location: {
    x: 1,
    y: 1,
  },//commas are extremely important!
  draw: function() {
    console.log("draw");
  }
};

circle.draw()
// There are three members. draw is a function in an object which is called a method. the rest are properties. 100% fundamentally different learn this.

